import numpy as np

# Create two arrays
a = np.array([1, 2, 3, 4])
b = np.array([5, 6, 7, 8])

# Mathematical operations
add = a + b
sub = a - b
mul = a * b
div = a / b

# Advanced operations
square = np.square(a)
sqrt = np.sqrt(b)

# Output
print("Array a:", a)
print("Array b:", b)

print("\nAddition:", add)
print("Subtraction:", sub)
print("Multiplication:", mul)
print("Division:", div)

print("\nSquare of a:", square)
print("Square root of b:", sqrt)